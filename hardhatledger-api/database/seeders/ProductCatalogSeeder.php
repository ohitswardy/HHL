    // Maps CSV category slugs → display names
    private const CATEGORY_MAP = [
        'FastMoving'        => 'Fast Moving',
        'Woodworks'         => 'Woodworks',
        'Paintworks'        => 'Paintworks',
        'MasonryFinishing'  => 'Masonry & Finishing',
        'SanitaryWaterline' => 'Sanitary & Waterline',
        'Electrical'        => 'Electrical',
        'Consumables'       => 'Consumables',
        'SteelRoofing'      => 'Steel & Roofing',
    ];

    // SKU prefix per category slug
    private const SKU_PREFIX = [
        'FastMoving'        => 'FM',
        'Woodworks'         => 'WW',
        'Paintworks'        => 'PT',
        'MasonryFinishing'  => 'MF',
        'SanitaryWaterline' => 'SW',
        'Electrical'        => 'EL',
        'Consumables'       => 'CN',
        'SteelRoofing'      => 'SR',
    ];

    public function run(): void
    {
        $categories   = $this->ensureCategories();
        $retailTier   = ClientTier::where('name', 'Retail')->first();
        $wholesaleTier = ClientTier::where('name', 'Wholesale')->first();

        $skuCounters = [];

        foreach ($this->products() as [$name, $catSlug, $cost, $retail, $hardware]) {
            // Derive unit
            $unit         = $this->detectUnit($name, $retail);
            $retailPrice  = $this->parsePrice($retail);
            $hwPrice      = $this->parsePrice($hardware);

            // Generate unique SKU
            $prefix  = self::SKU_PREFIX[$catSlug] ?? 'XX';
            $skuCounters[$catSlug] = ($skuCounters[$catSlug] ?? 0) + 1;
            $sku     = $prefix . '-' . str_pad($skuCounters[$catSlug], 4, '0', STR_PAD_LEFT);

            $product = Product::create([
                'sku'                => $sku,
                'name'               => $name,
                'category_id'        => $categories[$catSlug] ?? null,
                'unit'               => $unit,
                'cost_price'         => (float) $cost,
                'base_selling_price' => $retailPrice,
                'is_active'          => true,
                'branch_id'          => null,
            ]);

            InventoryStock::create([
                'product_id'         => $product->id,
                'quantity_on_hand'   => 0,
                'quantity_reserved'  => 0,
                'branch_id'          => null,
            ]);

            if ($retailTier && $retailPrice > 0) {
                ProductPrice::create([
                    'product_id'     => $product->id,
                    'client_tier_id' => $retailTier->id,
                    'price'          => $retailPrice,
                ]);
            }

            if ($wholesaleTier && $hwPrice > 0) {
                ProductPrice::create([
                    'product_id'     => $product->id,
                    'client_tier_id' => $wholesaleTier->id,
                    'price'          => $hwPrice,
                ]);
            }
        }
    }

    // ─── Helpers ────────────────────────────────────────────────────────────

    private function ensureCategories(): array
    {
        $result = [];
        foreach (self::CATEGORY_MAP as $slug => $displayName) {
            $cat          = Category::firstOrCreate(['name' => $displayName]);
            $result[$slug] = $cat->id;
        }
        return $result;
    }

    /** Parse a price value which may be numeric, 'REAL TIME', 'X/bdft', 'X/linear', or '₱X,XXX' */
    private function parsePrice(mixed $price): float
    {
        if (is_numeric($price)) {
            return (float) $price;
        }
        $str = trim((string) $price);
        if ($str === '' || stripos($str, 'REAL TIME') !== false) {
            return 0.0;
        }
        // X/bdft or X/linear
        if (preg_match('/^([\d.]+)\/(bdft|linear|linar)/i', $str, $m)) {
            return (float) $m[1];
        }
        // Strip currency symbol and thousands separators
        $clean = preg_replace('/[^\d.]/', '', $str);
        return is_numeric($clean) ? (float) $clean : 0.0;
    }

    /** Determine unit of measure from product name and retail price string */
    private function detectUnit(string $name, mixed $retailPrice): string
    {
        $r = trim((string) $retailPrice);
        if (preg_match('/\/bdft/i', $r))   return 'bdft';
        if (preg_match('/\/linear/i', $r)) return 'linear';
        if (preg_match('/\/linar/i', $r))  return 'linear';

        $n = strtolower($name);

        // BOX / ROLL products
        if (str_starts_with($n, 'box '))  return 'box';
        if (str_starts_with($n, 'roll ')) return 'roll';

        // Explicit unit suffixes at end of name
        $suffixes = [
            ' kg'   => 'kg',
            ' bag'  => 'bag',
            ' gal'  => 'gal',
            ' tin'  => 'tin',
            ' mtr'  => 'mtr',
            ' btl'  => 'btl',
            ' l'    => 'L',     // catch " L" at end
            ' 1l'   => 'L',
            ' 1/4l' => 'L',
        ];
        foreach ($suffixes as $suffix => $unit) {
            if (str_ends_with($n, $suffix)) return $unit;
        }

        // Inline suffixes anywhere in name
        if (preg_match('/\bkg\b/i', $name))  return 'kg';
        if (preg_match('/\bgal\b/i', $name)) return 'gal';
        if (preg_match('/\btin\b/i', $name)) return 'tin';
        if (preg_match('/\bmtr\b/i', $name)) return 'mtr';
        if (preg_match('/\bbtl\b/i', $name)) return 'btl';
        if (preg_match('/\bbag\b/i', $name)) return 'bag';
        if (preg_match('/\broll\b/i', $name)) return 'roll';

        return 'pc';
    }

